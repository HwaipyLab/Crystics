package com.hwaipy.quantity;

/**
 *
 * @author Hwaipy 2015-3-15
 */
public enum SIBaseUnit {

  /**
   * The distance travelled by light in vacuum in 1/299792458 second.(1983)
   */
  m,
  /**
   * The mass of the international prototype kilogram.(1889)
   */
  kg,
  /**
   * The duration of 9192631770 periods of the radiation corresponding to the
   * transition between the two hyperfine levels of the ground state of the
   * caesium 133 atom.(1967)
   */
  s,
  /**
   * The constant current which, if maintained in two straight parallel
   * conductors of infinite length, of negligible circular cross-section,
   * and placed 1 m apart in vacuum, would produce between these conductors
   * a force equal to 2×10^−7 newtons per metre of length.(1946)
   */
  A,
  /**
   * 1/273.16 of the thermodynamic temperature of the triple point of water.(1967)
   */
  K,
  /**
   * The amount of substance of a system which contains as many elementary
   * entities as there are atoms in 0.012 kilogram of carbon 12.(1967)
   */
  mol,
  /**
   * The luminous intensity, in a given direction, of a source that emits
   * monochromatic radiation of frequency 540×1012 hertz and that has a
   * radiant intensity in that direction of 1/683 watt per steradian.(1979)
   */
  cd;
}
